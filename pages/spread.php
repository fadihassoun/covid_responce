    <h2>How Covid-19 spreads</h2>
    
    <p>
    Covid-19 can spread among people in different ways.
    </p>

    <ul>
        <li><b>Close contact:</b> <span> COVID-19 is mainly spread through the very small air droplets coming from the nose or the mouth of an infected person when they cough, sneeze or talk. A person in the vicinity can catch the infection when these droplets reach their eyes, nose or mouth.<br><br>

        The longer you spend close to an infected person, the greater the chance the virus will spread to you. Keeping a distance of 1 to 2 metres from other people lowers the risk of catching COVID-19 and reduces its spread. </span></li><br>

        <li><b>Airborne spread:</b> <span> The tiny droplets from an infected person's cough, sneeze, or talk can remain suspended in the air for longer periods and travel longer distances in crowded indoor places with poor ventilation. <br><br>

        You can be much safer if you meet people outdoors rather than indoors and avoid crowded or stuffy places. Ventilate indoor spaces through windows, doors and vents to the maximum when you meet people indoors. </span> </li> <br>

        <li><b>Infected surfaces:</b> <span> Although it is less common, if you touch an infected surface where droplets from an infected person have fallen and then touch your face, you could become infected too. <br><br>

        To reduce this risk, wash your hands with soap regularly and clean surfaces with household detergents.</span> </li>                     
    </ul>
